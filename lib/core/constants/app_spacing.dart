/// Shared app spacing tokens.
abstract final class AppSpacing {
  /// Extra small spacing.
  static const xs = 4.0;

  /// Small spacing.
  static const sm = 8.0;

  /// Medium spacing.
  static const md = 16.0;

  /// Large spacing.
  static const lg = 24.0;

  /// Extra large spacing.
  static const xl = 32.0;

  /// Navigation bar height.
  static const navHeight = 64.0;
}
